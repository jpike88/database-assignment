DELIMITER $$

CREATE PROCEDURE move_train_to_station(IN selectedTrainID INT, IN selectedStationID INT)
BEGIN
	SET @trainOID = (SELECT oid FROM Train WHERE tid = selectedTrainID LIMIT 1);

	UPDATE Operator
			SET Operator.stationsTravelled = Operator.stationsTravelled + 1 
			WHERE Operator.oid = @trainOID;

	UPDATE Train
			SET Train.stationsTravelled = Train.stationsTravelled + 1,
			Train.sid = selectedStationID
			WHERE Train.tid = selectedTrainID;

END$$
DELIMITER ;

-- Trigger: Set customer to isAvailable
DELIMITER $$

CREATE TRIGGER ticket_customer_insert
AFTER INSERT ON Ticket
FOR EACH ROW
BEGIN
        IF NEW.cid IS NOT NULL THEN
            UPDATE Customer SET Customer.isAvailable = 0 WHERE Customer.cid = NEW.cid;
        END IF;
END$$

DELIMITER ;


-- Trigger: Set customer to isAvailable
DELIMITER $$

CREATE TRIGGER ticket_customer_update 
AFTER UPDATE ON Ticket
FOR EACH ROW
BEGIN
        IF NEW.cid IS NOT NULL AND NEW.stationDisembarked IS NOT NULL THEN
            UPDATE Customer SET Customer.isAvailable = 1 WHERE Customer.cid = NEW.cid;
        END IF;
END$$

